package loops.in.com;

public class unary {
	public static void main(String[] args) {
		int a=10;
				int b=8;
		System.out.println(--a);
		System.out.println(--b);
		System.out.println(b--);
		System.out.println(a--);
		System.out.println(++a);
		System.out.println(++b);
		System.out.println(a++);
		System.out.println(b++);
	}

}
